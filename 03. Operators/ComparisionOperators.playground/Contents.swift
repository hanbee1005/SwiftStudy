import UIKit

/*
 # Comparison Operators
 비교 연산자
 이항 연산자이며 결과값은 항상 Bool 타입
 */

let a = 12
let b = 34

// Equal to Operator
// ex) a == b
// 피연산자의 자료형을 같게 해야 비교 가능
a == b
"Swift" == "swift"

// Not Equal to Operator
// ex) a != b
a != b

// Greater than Operator
// ex) a > b
a > b
"swift" > "Swift" // ASCII 문자로 비교

// Greater than or equal to Operator
// ex) a >= b
a >= b

// Less than Operator
// ex) a < b
a < b

// Less than or equal to Operator
// ex) a <= b
a <= b
